#include "3-calc.h"

/**
 * op_add - Adds a to b.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: Addition of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - ubtracts b from a.
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: Subtraction of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}